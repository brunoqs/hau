//-----------------------------------------------------------------------------
// Requirements
//-----------------------------------------------------------------------------

var util = require('./util');

//-----------------------------------------------------------------------------
// Public
//-----------------------------------------------------------------------------

function track (client, id, action) {
  client.setbit(util.keyFor(action), id, 1);
}

function untrack (client, id, action) {
  client.setbit(util.keyFor(action), id, 0);
}

//-----------------------------------------------------------------------------
// Exports
//-----------------------------------------------------------------------------

exports.track = track;
exports.untrack = untrack;
